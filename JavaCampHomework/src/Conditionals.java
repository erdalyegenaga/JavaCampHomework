public class Conditionals {
    public static void main(String[] args) {
        int number = 38;
        if (number < 38) {
            System.out.println("The number is less than 38.");
        }else if (number == 38) {
            System.out.println("The number is 38.");
        }else {
            System.out.println("The number is greater than 38.");
        }
    }
}

